// Auto-detect browser language on first visit
function detectInitialLanguage() {
  try {
    const saved = localStorage.getItem('relaypay_language');
    if (saved) return saved;
    const browserLang = (navigator.language || navigator.userLanguage || 'es').toLowerCase();
    if (browserLang.startsWith('en')) return 'en';
    if (browserLang.startsWith('es')) return 'es';
    return 'es';
  } catch (e) {
    return 'es';
  }
}

let currentLang = detectInitialLanguage();
try { localStorage.setItem('relaypay_language', currentLang); } catch (e) {}

export function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || I18N.en[key] || key;
}

export function setLanguage(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  try { localStorage.setItem('relaypay_language', lang); } catch (e) {}
  document.documentElement.lang = lang;
  applyTranslations();
}

export function getLanguage() {
  return currentLang;
}

export function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
}

export function changeLanguage(lang) {
  setLanguage(lang);
  // Show toast + re-render current view
  if (typeof window !== 'undefined') {
    window.dispatchEvent(new CustomEvent('app:language-changed', { detail: { lang } }));
  }
}

// Initialize document lang on load
if (typeof document !== 'undefined') {
  document.documentElement.lang = currentLang;
  // Set up MutationObserver for live translations (throttled)
  let isApplying = false;
  const observer = new MutationObserver(() => {
    if (isApplying) return;
    isApplying = true;
    setTimeout(() => {
      applyTranslations();
      isApplying = false;
    }, 100);
  });
  if (document.body) {
    observer.observe(document.body, { childList: true, subtree: true });
  } else {
    document.addEventListener('DOMContentLoaded', () => {
      observer.observe(document.body, { childList: true, subtree: true });
    });
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyTranslations);
  } else {
    setTimeout(applyTranslations, 100);
  }
}

// Expose to window for backward compatibility (used by inline HTML attributes)
if (typeof window !== 'undefined') {
  window.I18N = I18N;
  window.i18n = I18N;
  window.t = t;
  window.setLanguage = setLanguage;
  window.getLanguage = getLanguage;
  window.applyTranslations = applyTranslations;
  window.changeLanguage = changeLanguage;
}